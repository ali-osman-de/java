public class PreMidtermQuesiton_1 {

    public static void main(String[] args) {
        for (int x = 5; x > 0; x--) {
            for (int y = 0; y < x ; y++) {
                System.out.printf("*");
            }
            System.out.println("");
        }
        for (int x = 0; x < 5; x++) {
            for (int y = 0; y < x + 1; y++) {
                System.out.printf("*");
            }
            System.out.println("");
}
    }
}
