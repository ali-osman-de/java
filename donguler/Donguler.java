package com.mycompany.donguler;

/**
 *
 * @author Ali Osman
 */
public class Donguler {

    public static void main(String[] args) {
        
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti!");
        
                System.out.println("-----------------------------------------------------");
        
        int i = 0;
        
        while (i < 10) {            
            System.out.println(i);
            i++;
        }
        System.out.println("While Döngüsü Bitti!");
        System.out.println("----------------------------------------------------------------");
        
        do {            
            System.out.println(i);
            i++;
        } while (i < 10);
        
        
        
    }
}
